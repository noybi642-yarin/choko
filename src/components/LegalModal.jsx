export const LEGAL_CONTENT = { terms: TERMS, privacy: PRIVACY };

export default function LegalModal({ type, onClose }) {
  const isTerms = type === 'terms';
  const title = isTerms ? 'תנאי שימוש – Choko' : 'מדיניות פרטיות – Choko';
  const body   = isTerms ? TERMS : PRIVACY;

  return (
    <div className="modal-overlay" onClick={e => e.target === e.currentTarget && onClose()}>
      <div className="modal-box legal-modal">
        <div className="modal-header">
          <h2>{title}</h2>
          <button className="modal-close" onClick={onClose}>✕</button>
        </div>
        <div className="legal-body">
          {body.split('\n').map((line, i) => {
            const trimmed = line.trim();
            if (!trimmed) return <div key={i} className="legal-spacer" />;
            // Section headers: lines like "1. מהות השירות"
            if (/^\d+\./.test(trimmed)) return <h3 key={i} className="legal-section">{trimmed}</h3>;
            // Main title
            if (i === 0) return <h2 key={i} className="legal-title">{trimmed}</h2>;
            // Subtitle line (date)
            if (i === 1) return <p key={i} className="legal-updated">{trimmed}</p>;
            // Bullet points
            if (trimmed.startsWith('•')) return <div key={i} className="legal-bullet">{trimmed}</div>;
            return <p key={i} className="legal-para">{trimmed}</p>;
          })}
        </div>
        <div className="legal-footer">
          <button className="btn btn-primary" onClick={onClose}>הבנתי, סגור</button>
        </div>
      </div>
    </div>
  );
}
